#include "libft.h"

int	ft_isalpha(int c)
{
	int	alpha;

	alpha = 1;
	if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
		alpha = 0;
	return (alpha);
}
