#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	int		counter;
	size_t	i;

	i = 0;
	counter = 0;
	while (src[counter])
		counter++;
	if (!size)
		return (counter);
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (counter);
}
