#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	j;
	char	*str;

	i = 0;
	if (ft_strlen(s) < len)
		str = (char *)malloc(sizeof(*s) * (ft_strlen(s) + 1));
	else
		str = (char *)malloc(sizeof(*s) * (len + 1));
	if (!str || !s)
		return (0);
	i = 0;
	j = 0;
	while (s[i])
	{
		if (i >= start && j < len)
		{
			str[j] = s[i];
			j++;
		}
		i++;
	}
	str[j] = '\0';
	return (str);
}
